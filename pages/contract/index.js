/**
 * 将 Markdown 文本转为 HTML 字符串，供 rich-text 组件渲染。
 * 支持：<h2>标题、**加粗**、普通段落、表格。
 * 连续空行合并为一个，避免留白过大。
 */
function markdownToHtml(text) {
  const lines = text.split('\n');
  const html = [];
  let tableRows = []; // 收集连续的表格行

  function flushTable() {
    if (tableRows.length === 0) return;
    let tableHtml = '<table border="1" cellpadding="6" cellspacing="0" style="border-collapse:collapse;width:100%;font-size:24rpx;color:#666;margin:8rpx 0;">';
    tableRows.forEach((row, idx) => {
      const tag = idx === 0 ? 'th' : 'td';
      const bgStyle = idx === 0 ? ' style="background:#f5f5f5;font-weight:600;color:#333;"' : '';
      const cellsHtml = row.map(c => `<${tag}${bgStyle}>${c}</${tag}>`).join('');
      tableHtml += `<tr>${cellsHtml}</tr>`;
    });
    tableHtml += '</table>';
    html.push(tableHtml);
    tableRows = [];
  }

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const trimmed = line.trim();

    // 跳过空行，连续空行只保留一个换行
    if (!trimmed) {
      // 如果有待刷新的表格，先刷新
      flushTable();
      const last = html[html.length - 1];
      if (last && last !== '<br/>') {
        html.push('<br/>');
      }
      continue;
    }

    // 已是 HTML 标题，直接使用
    if (trimmed.startsWith('<h2>')) {
      flushTable();
      html.push(trimmed);
    } else if (trimmed.startsWith('|')) {
      // 检测表格
      const cells = trimmed.split('|').filter(c => c.trim() !== '');
      if (cells.length === 0) continue;
      const isSeparator = cells.every(c => /^[\s-]+$/.test(c));
      if (isSeparator) continue; // 跳过表头分隔行
      tableRows.push(cells.map(c => c.trim()));
    } else {
      flushTable();
      // 普通段落
      let result = trimmed.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>');
      html.push(`<p>${result}</p>`);
    }
  }

  // 刷新剩余表格行
  flushTable();

  return html.join('');
}

